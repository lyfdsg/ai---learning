def capitalize_first(s: str) -> str:
    """将字符串的第一个字母大写""" 
    # 这个函数的作用是将字符串的第一个字母转换为大写，而其他字母保持不变。
    # 比如 "hello world" 会变成 "Hello world"，而 "hELLO wORLD" 会变成 "HELLO wORLD"。
    if not s:
        return s
    return s[0].upper() + s[1:]
    #这个代码的意思是：将字符串的第一个字母转换为大写，而其他字母保持不变。
    # 解释s[0].upper() + s[1:]：s[0] 是字符串的第一个字符，s[1:] 是字符串的剩余部分。
